      let scrollAttempts = 0;
      let animationsComplete = false;
      let isProcessing = false;

      // Initially hide heading and images
      if (heading) heading.style.opacity = '0';
      images.forEach(img => img.style.opacity = '0');

      // Logo animation (appear in center)
      if (logo) {
        setTimeout(() => {
          logo.classList.add('top-center-logo');
          console.log("Logo animation triggered with top-center-logo");
        }, 500);
      }

      // Function to handle scroll movements for initial animations
      const handleScrollAttempt = (e) => {
        e.preventDefault();
        if (isProcessing || animationsComplete) return;

        isProcessing = true;
        console.log('Scroll attempt:', scrollAttempts);

        if (scrollAttempts === 0) {
          console.log("First scroll: Moving logo from center to top-left");
          if (logo && headerLogoContainer) {
            // Remove existing content in header logo container
            headerLogoContainer.innerHTML = '';
            // Move the logo to the header
            headerLogoContainer.appendChild(logo);
            // Apply the move-to-top-left class for positioning and sizing
            logo.classList.remove('top-center-logo'); // Remove center positioning
            logo.classList.add('move-to-top-left');
            console.log("Logo moved to top-left with width: 180px, height: auto");
          }
          setTimeout(() => {
            console.log("Heading displayed after 1s delay");
            if (heading) {
              heading.classList.add('animate');
            }
            scrollAttempts++;
            isProcessing = false;
          }, 1000);
        } else if (scrollAttempts === 1) {
          console.log("Second scroll: Images displayed and page scrolling unlocked");
          images.forEach((img) => {
            img.classList.add('animate', 'hover-effect');
            console.log(`Animating Image to position:`, img.src);
            if (getComputedStyle(img).opacity === '1') console.log(`Image successfully animated`);
            else console.error(`Image failed to animate`);
          });
          // Unlock page scrolling immediately after starting the image animation
          animationsComplete = true;
          document.body.style.overflow = 'auto';
          console.log("Page scrolling enabled");
          isProcessing = false;
        }
      };

      // Handle next section animation
      const aboutObserver = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
          if (entry.isIntersecting) {
            const title = entry.target.querySelector('.section-title');
            title.classList.add('animate');
            let delay = 500;
            const boxes = entry.target.querySelectorAll('.col-md-4');
            boxes.forEach((box, index) => {
