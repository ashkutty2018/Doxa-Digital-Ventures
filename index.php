            cards.forEach((card, index) => {
              setTimeout(() => {
                if (index % 2 === 0) {
                  card.parentElement.classList.add('slide-left');
                } else {
                  card.parentElement.classList.add('slide-right');
                }
              }, index * 200);
            });
            observer.unobserve(entry.target);
          }
        });
