          ) {
            sidebar.classList.remove("active");
          }
        });
      }
  
      // About Section Animation
      const aboutObserver = new IntersectionObserver(
        (entries, observer) => {
          entries.forEach((entry) => {
            if (entry.isIntersecting) {
              const title = entry.target.querySelector(".section-title");
              const texts = entry.target.querySelectorAll(".section-text");
              const cols = entry.target.querySelectorAll(".col-md-6");
  
              cols.forEach((col) => (col.style.opacity = "1"));
              if (title) title.classList.add("animate");
              texts.forEach((text, index) => {
                setTimeout(() => text.classList.add("animate"), index * 300);
              });
  
              observer.unobserve(entry.target);
            }
          });
        },
        { threshold: 0.5 }
      );
  
      aboutObserver.observe(document.querySelector("#about"));
  
      // Leader Section Animation
      const leaderObserver = new IntersectionObserver(
        (entries, observer) => {
          entries.forEach((entry) => {
            if (entry.isIntersecting) {
              const title = entry.target.querySelector(".section-title");
              const texts = entry.target.querySelectorAll(".section-text");
